create table Book
(bookId integer GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
name varchar(25) not null,
author varchar(25),
publication varchar(25),
description varchar(50),
noOfCopies integer,
primary key (bookId));


create table Student
(studentId int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
firstName varchar(25) not null,
lastName varchar(25),
address varchar(50),
phoneNumber integer,
email varchar(50),
primary key(studentId));

create table BookRecord 
(
bookRecordId int  GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1)  NOT NULL,
bookId integer ,
status varchar(20),
studentId varchar(20), 
CONSTRAINT ps foreign key(bookId) references Book(bookId));